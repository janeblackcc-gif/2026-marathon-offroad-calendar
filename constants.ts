export const RAW_EVENTS: MarathonEvent[] = RAW_DATA_STR
  .trim()
  .split('\n')
  .map((line) => {
    const parts = line.split('|').map((s) => s.trim());
    const idMatch = parts[0].match(/^(\d+)\.\s*(.*)$/);
    if (!idMatch) return null;
    return {
      id: parseInt(idMatch[1]),
      province: idMatch[2],
      name: parts[1],
      time: parts[2],
      organizer: parts[3],
      category: parts[4] as EventCategory,
      eventType: detectType(parts[1])
    };
  })
  .filter((e): e is MarathonEvent => e !== null);

export const groupEventsByDate = (events: MarathonEvent[]): { [key: string]: MarathonEvent[] } => {
  const groups: { [key: string]: MarathonEvent[] } = {};
  events.forEach(event => {
    const dateKey = event.time;
    if (!groups[dateKey]) groups[dateKey] = [];
    groups[dateKey].push(event);
  });
  return groups;
};

export const sortDates = (a: string, b: string): number => {
  if (a === b) return 0;
  if (a === '待定') return 1;
  if (b === '待定') return -1;
  
  const getWeight = (s: string) => {
    const m = s.match(/(\d+)月/);
    const d = s.match(/(\d+)日/);
    const month = m ? parseInt(m[1]) : 0;
    const day = d ? parseInt(d[1]) : 0;
    // 只有月份的日期排在该月具体日期的最后
    if (!d && m) return month * 100 + 99;
    return month * 100 + day;
  };
  
  return getWeight(a) - getWeight(b);
};
